
class LeftShiftDemo {

	public static void main(String[]args){

		int num1=12;
		int num2=75;
		int num3=-12;
		int num4=-75;

		int res=0;

		res=num1<<2;
		System.out.println(res);	//48

		res=num2<<4;
		System.out.println(res);	//1200

		res=num3<<2;
		System.out.println(res);	//-48

		res=num4<<4;
		System.out.println(res);	//-1200

	}

}

