
class RightShiftDemo{

	public static void main(String[]args){

		int num1=95;
		int num2=-95;
		int num3=60;
		int num4=-60;
		int num5=-125;

		int res=0;

		res=num1>>4;
		System.out.println(res);
		
		res=num2>>4;
		System.out.println(res);
	
		res=num3>>4;
		System.out.println(res);

		res=num4>>4;
		System.out.println(res);

		res=num5>>5;
		System.out.println(res);

	}

}
